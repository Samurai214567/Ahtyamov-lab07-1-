# Рекурсивная функция для генерации всех бинарных строк длины n
def generate_binary_strings(current: str, n: int):
    """
    current — уже построенная часть строки
    n       — общая длина бинарной строки
    """

    # Базовый случай:
    # если длина текущей строки равна n,
    # значит, строка полностью сформирована — выводим её
    if len(current) == n:
        print(current)
        return

    # Рекурсивный случай:
    # 1. Добавляем '0' и продолжаем строить строку
    generate_binary_strings(current + "0", n)

    # 2. Добавляем '1' и продолжаем строить строку
    generate_binary_strings(current + "1", n)


if __name__ == "__main__":
    # Ввод длины бинарных строк
    n = int(input("Введите длину бинарных строк n: "))

    print(f"Все бинарные строки длины {n}:")
    # Начинаем с пустой строки
    generate_binary_strings("", n)
