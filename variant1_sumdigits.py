# Рекурсивная функция для вычисления суммы цифр числа
def sum_digits(n: int) -> int:
    # Если число отрицательное — преобразуем в положительное
    if n < 0:
        n = -n

    # Базовый случай:
    # когда n == 0, цифры закончились
    if n == 0:
        return 0

    # Рекурсивный случай:
    # берём последнюю цифру (n % 10)
    # вызываем функцию для оставшихся цифр (n // 10)
    return n % 10 + sum_digits(n // 10)


if __name__ == "__main__":
    # Ввод числа
    n = int(input("Введите число: "))

    # Вывод результата работы рекурсии
    print("Сумма цифр =", sum_digits(n))
