# Рекурсивная функция для переворота строки
def reverse_string(s: str) -> str:
    # Базовый случай:
    # если строка пустая или состоит из одного символа — возвращаем её
    if len(s) <= 1:
        return s

    # Рекурсивный случай:
    # reverse(s[1:]) — переворачиваем всё, кроме первого символа
    # s[0] — ставим первый символ в конец
    return reverse_string(s[1:]) + s[0]


if __name__ == "__main__":
    # Ввод строки от пользователя
    s = input("Введите строку: ")

    # Вывод результата
    print("Перевёрнутая строка:", reverse_string(s))
