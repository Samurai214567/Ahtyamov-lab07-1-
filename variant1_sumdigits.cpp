#include <iostream>
using namespace std;

// Рекурсивная функция, которая вычисляет сумму цифр числа
int sumDigits(int n) {
    // Если число отрицательное — делаем его положительным
    if (n < 0) n = -n;

    // Базовый случай рекурсии:
    // когда число стало 0 — больше цифр нет
    if (n == 0) return 0;

    // Рекурсивный случай:
    // 1) берем последнюю цифру (n % 10)
    // 2) добавляем к сумме остальных цифр (sumDigits(n / 10))
    return n % 10 + sumDigits(n / 10);
}

int main() {
    int n;

    // Ввод числа от пользователя
    cout << "Введите число: ";
    cin >> n;

    // Вызов рекурсивной функции и вывод результата
    cout << "Сумма цифр = " << sumDigits(n) << endl;

    return 0;
}
